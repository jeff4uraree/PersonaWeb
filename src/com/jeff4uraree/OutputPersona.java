package com.jeff4uraree;

public class OutputPersona {

	private Integer personaID;
	private String personaName;
	private Integer arcanaID;
	private String arcanaType;
	private Integer level;
	
	public Integer getPersonaID() {
		return personaID;
	}
	public void setPersonaID(Integer personaID) {
		this.personaID = personaID;
	}
	public String getPersonaName() {
		return personaName;
	}
	public void setPersonaName(String personaName) {
		this.personaName = personaName;
	}
	public Integer getArcanaID() {
		return arcanaID;
	}
	public void setArcanaID(Integer arcanaID) {
		this.arcanaID = arcanaID;
	}
	public String getArcanaType() {
		return arcanaType;
	}
	public void setArcanaType(String arcanaType) {
		this.arcanaType = arcanaType;
	}
	public Integer getLevel() {
		return level;
	}
	public void setLevel(Integer level) {
		this.level = level;
	}
}
